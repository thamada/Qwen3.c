/* ================================================================
 * Quantization Block Structures (packed, exact GGML layouts)
 * ================================================================ */

#pragma pack(push, 1)
typedef struct {
    uint16_t d;       /* super-block scale (FP16, for quantized scales) */
    uint16_t dmin;    /* super-block scale (FP16, for quantized mins)   */
    uint8_t  scales[K_SCALE_SIZE]; /* 6-bit packed scales+mins */
    uint8_t  qs[QK_K / 2];         /* 4-bit quants */
} BlockQ4_K;                       /* 144 bytes */

typedef struct {
    uint16_t d;
    uint16_t dmin;
    uint8_t  scales[K_SCALE_SIZE];
    uint8_t  qh[QK_K / 8];         /* 5th bit */
    uint8_t  qs[QK_K / 2];         /* low 4 bits */
} BlockQ5_K;                       /* 176 bytes */

typedef struct {
    uint16_t d;
    uint8_t  qs[QK_K / 4];         /* first half: 8-bit grid lookup low; second half: sign bits */
    uint8_t  qh[QK_K / 32];        /* upper 2 bits of grid lookup */
    uint8_t  scales[QK_K / 32];    /* 4-bit packed scale per 32-element sub-block */
} BlockIQ2_S;                      /* 82 bytes */

typedef struct {
    uint16_t d;
    uint8_t  qs[QK_K / 4];         /* 8-bit grid low */
    uint8_t  qh[QK_K / 32];        /* high bit       */
    uint8_t  signs[QK_K / 8];      /* one byte per 8 weights */
    uint8_t  scales[QK_K / 64];    /* 4-bit packed scale per 64-element pair */
} BlockIQ3_S;                      /* 110 bytes */
#pragma pack(pop)

