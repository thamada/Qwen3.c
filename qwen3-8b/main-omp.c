/* ================================================================
 * FP16 <-> FP32  (host)
 * ================================================================ */

static inline float host_f16f32(uint16_t h) {
    uint32_t sgn = ((uint32_t)(h & 0x8000)) << 16;
    int exp = (h >> 10) & 0x1F;
    uint32_t man = h & 0x3FF;
    uint32_t f;
    if (exp == 0) {
        if (man == 0) { f = sgn; }
        else {
            exp = 1;
            while (!(man & 0x400)) { man <<= 1; exp--; }
            man &= 0x3FF;
            f = sgn | ((uint32_t)(exp + 112) << 23) | (man << 13);
        }
    } else if (exp == 31) {
        f = sgn | 0x7F800000u | (man << 13);
    } else {
        f = sgn | ((uint32_t)(exp + 112) << 23) | (man << 13);
    }
    float r;
    memcpy(&r, &f, 4);
    return r;
}

static inline uint16_t host_f32f16(float f) {
    uint32_t u;
    memcpy(&u, &f, 4);
    uint32_t sgn = (u >> 16) & 0x8000u;
    int      exp = (int)((u >> 23) & 0xFF) - 127 + 15;
    uint32_t man = u & 0x7FFFFFu;
    uint16_t h;
    if (exp >= 31) {
        h = (uint16_t)(sgn | 0x7C00u | (man ? 0x200u : 0));
    } else if (exp <= 0) {
        if (exp < -10) {
            h = (uint16_t)sgn;
        } else {
            man = (man | 0x800000u) >> (1 - exp);
            uint32_t rnd = (man & 0x1000u) ? 1u : 0u;
            h = (uint16_t)(sgn | ((man >> 13) + rnd));
        }
    } else {
        uint32_t rnd = (man & 0x1000u) ? 1u : 0u;
        h = (uint16_t)(sgn | ((uint32_t)exp << 10) | (man >> 13));
        h = (uint16_t)(h + rnd);
    }
    return h;
}

/* ================================================================
 * CPU dequantizers (Q4_K, Q5_K, IQ2_S, IQ3_S) → FP32 buffer
 * ================================================================ */

static inline void get_scale_min_k4(int j, const uint8_t *q, uint8_t *d, uint8_t *m) {
    if (j < 4) {
        *d = q[j] & 63;
        *m = q[j + 4] & 63;
    } else {
        *d = (q[j + 4] & 0xF) | ((q[j - 4] >> 6) << 4);
        *m = (q[j + 4] >> 4)  | ((q[j - 0] >> 6) << 4);
    }
}

static void dequant_q4_k(const BlockQ4_K *x, float *y, int64_t nb) {
    for (int64_t i = 0; i < nb; i++) {
        const uint8_t *q = x[i].qs;
        const float d   = host_f16f32(x[i].d);
        const float min = host_f16f32(x[i].dmin);
        int is = 0;
        uint8_t sc, m;
        for (int j = 0; j < QK_K; j += 64) {
            get_scale_min_k4(is + 0, x[i].scales, &sc, &m);
            const float d1 = d * sc; const float m1 = min * m;
            get_scale_min_k4(is + 1, x[i].scales, &sc, &m);
            const float d2 = d * sc; const float m2 = min * m;
            for (int l = 0; l < 32; ++l) *y++ = d1 * (q[l] & 0xF) - m1;
            for (int l = 0; l < 32; ++l) *y++ = d2 * (q[l] >> 4)  - m2;
            q += 32; is += 2;
        }
    }
}

static void dequant_q5_k(const BlockQ5_K *x, float *y, int64_t nb) {
    for (int64_t i = 0; i < nb; i++) {
        const uint8_t *ql = x[i].qs;
        const uint8_t *qh = x[i].qh;
        const float d   = host_f16f32(x[i].d);
        const float min = host_f16f32(x[i].dmin);
        int is = 0;
        uint8_t sc, m;
        uint8_t u1 = 1, u2 = 2;
        for (int j = 0; j < QK_K; j += 64) {
            get_scale_min_k4(is + 0, x[i].scales, &sc, &m);
            const float d1 = d * sc; const float m1 = min * m;
            get_scale_min_k4(is + 1, x[i].scales, &sc, &m);
            const float d2 = d * sc; const float m2 = min * m;
            for (int l = 0; l < 32; ++l) *y++ = d1 * ((ql[l] & 0xF) + ((qh[l] & u1) ? 16 : 0)) - m1;
            for (int l = 0; l < 32; ++l) *y++ = d2 * ((ql[l] >> 4)  + ((qh[l] & u2) ? 16 : 0)) - m2;
            ql += 32; is += 2;
            u1 <<= 2; u2 <<= 2;
        }
    }
}

static void dequant_iq2_s(const BlockIQ2_S *x, float *y, int64_t nb) {
    float db[2];
    for (int64_t i = 0; i < nb; i++) {
        const float d = host_f16f32(x[i].d);
        const uint8_t *qs = x[i].qs;
        const uint8_t *qh = x[i].qh;
        const uint8_t *signs = qs + QK_K / 8; /* second half of qs[] */
        for (int ib32 = 0; ib32 < QK_K / 32; ++ib32) {
            db[0] = d * (0.5f + (x[i].scales[ib32] & 0xf)) * 0.25f;
            db[1] = d * (0.5f + (x[i].scales[ib32] >> 4)) * 0.25f;
            for (int l = 0; l < 4; ++l) {
                const float dl = db[l / 2];
                const uint8_t *grid = (const uint8_t *)(iq2s_grid + (qs[l] | (((uint32_t)qh[ib32] << (8 - 2 * l)) & 0x300)));
                for (int j = 0; j < 8; ++j) {
                    y[j] = dl * grid[j] * ((signs[l] & kmask_iq2xs[j]) ? -1.f : 1.f);
                }
                y += 8;
            }
            qs += 4;
            signs += 4;
        }
    }
}

static void dequant_iq3_s(const BlockIQ3_S *x, float *y, int64_t nb) {
    for (int64_t i = 0; i < nb; i++) {
        const float d = host_f16f32(x[i].d);
        const uint8_t *qs    = x[i].qs;
        const uint8_t *qh    = x[i].qh;
        const uint8_t *signs = x[i].signs;
        for (int ib32 = 0; ib32 < QK_K / 32; ib32 += 2) {
            const float db1 = d * (1 + 2 * (x[i].scales[ib32 / 2] & 0xf));
            const float db2 = d * (1 + 2 * (x[i].scales[ib32 / 2] >> 4));
            for (int l = 0; l < 4; ++l) {
                const uint8_t *grid1 = (const uint8_t *)(iq3s_grid + (qs[2 * l + 0] | (((uint32_t)qh[0] << (8 - 2 * l)) & 256)));
                const uint8_t *grid2 = (const uint8_t *)(iq3s_grid + (qs[2 * l + 1] | (((uint32_t)qh[0] << (7 - 2 * l)) & 256)));
                for (int j = 0; j < 4; ++j) {
                    y[j + 0] = db1 * grid1[j] * ((signs[l] & kmask_iq2xs[j + 0]) ? -1.f : 1.f);
                    y[j + 4] = db1 * grid2[j] * ((signs[l] & kmask_iq2xs[j + 4]) ? -1.f : 1.f);
                }
                y += 8;
            }
            qs += 8;
            signs += 4;
            for (int l = 0; l < 4; ++l) {
                const uint8_t *grid1 = (const uint8_t *)(iq3s_grid + (qs[2 * l + 0] | (((uint32_t)qh[1] << (8 - 2 * l)) & 256)));
                const uint8_t *grid2 = (const uint8_t *)(iq3s_grid + (qs[2 * l + 1] | (((uint32_t)qh[1] << (7 - 2 * l)) & 256)));
                for (int j = 0; j < 4; ++j) {
                    y[j + 0] = db2 * grid1[j] * ((signs[l] & kmask_iq2xs[j + 0]) ? -1.f : 1.f);
                    y[j + 4] = db2 * grid2[j] * ((signs[l] & kmask_iq2xs[j + 4]) ? -1.f : 1.f);
                }
                y += 8;
            }
            qh += 2;
            qs += 8;
            signs += 4;
        }
    }
}
