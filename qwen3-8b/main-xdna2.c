#define XDNA_DIE(...) do { \
    fprintf(stderr, "%s:%d xdna: ", __FILE__, __LINE__); \
    fprintf(stderr, __VA_ARGS__); \
    fputc('\n', stderr); \
    exit(1); \
} while (0)

#define XDNACHK(expr) do { \
    int _r = (expr); \
    if (_r < 0) { \
        fprintf(stderr, "%s:%d xdna ioctl failed: %s (rc=%d errno=%d)\n", \
                __FILE__, __LINE__, strerror(errno), _r, errno); \
        exit(1); \
    } \
} while (0)

