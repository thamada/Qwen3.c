/* ============================================================================
 * ERT (Embedded Runtime) command packet layout — what we write into a CMD BO.
 *
 *   The amdxdna driver inherited the Xilinx ERT command ABI: a packet header
 *   word followed by a payload. The first 4-byte header is a bitfield (state
 *   in [0:4], custom in [5:11], count in [12:23], opcode in [23:27], reserved
 *   [28:30], type in [27:31]).  For NPU workloads the opcode is ERT_START_NPU
 *   (or ERT_START_CU for legacy CU-style execution) and the payload is the
 *   struct amdxdna_cmd_start_npu defined below.
 * ============================================================================ */

#define ERT_CMD_STATE_NEW            1U
#define ERT_CMD_STATE_QUEUED         2U
#define ERT_CMD_STATE_RUNNING        3U
#define ERT_CMD_STATE_COMPLETED      4U
#define ERT_CMD_STATE_ERROR          5U
#define ERT_CMD_STATE_ABORT          6U
#define ERT_CMD_STATE_SUBMITTED      7U
#define ERT_CMD_STATE_TIMEOUT        8U
#define ERT_CMD_STATE_NORESPONSE     9U

enum ert_cmd_opcode {
    ERT_START_CU                = 0,
    ERT_START_KERNEL            = 0,
    ERT_CONFIGURE               = 2,
    ERT_EXIT                    = 3,
    ERT_ABORT                   = 4,
    ERT_EXEC_WRITE              = 5,
    ERT_CU_STAT                 = 6,
    ERT_START_COPYBO            = 7,
    ERT_SK_CONFIG               = 8,
    ERT_SK_START                = 9,
    ERT_SK_UNCONFIG             = 10,
    ERT_INIT_CU                 = 11,
    ERT_START_FA                = 12,
    ERT_CLK_CALIB               = 13,
    ERT_MB_VALIDATE             = 14,
    ERT_ACCESS_TEST_C           = 15,
    ERT_ACCESS_TEST             = 16,
    ERT_CU_PROBE                = 17,
    ERT_CMD_CHAIN               = 19,
    ERT_START_NPU               = 20,
    ERT_START_NPU_PREEMPT       = 21,
    ERT_START_NPU_PREEMPT_ELF   = 22,
    ERT_CMD_CHAIN_NPU           = 0x18,
};

/* Packet header: matches Xilinx ert_packet layout. */
struct ert_packet_header {
    union {
        struct {
            uint32_t state   : 4;
            uint32_t unused  : 6;
            uint32_t extra_cu_masks : 2;
            uint32_t count   : 11;
            uint32_t opcode  : 5;
            uint32_t type    : 4;
        };
        uint32_t header;
    };
};

/* Payload for ERT_START_NPU — written immediately after the header word and
 * the variable CU-mask block (see amdxdna/include/uapi/drm/amdxdna_accel.h). */
struct amdxdna_cmd_start_npu {
    uint64_t buffer;          /* xdna virtual address of instruction buffer */
    uint32_t buffer_size;     /* size of instruction buffer (bytes) */
    uint32_t prop_count;      /* number of property words */
    uint32_t prop_args[];     /* property and kernel arguments */
};

struct amdxdna_cmd_chain {
    uint32_t command_count;
    uint32_t submit_index;
    uint32_t error_index;
    uint32_t reserved[3];
    uint64_t data[];
};

