/* ================================================================
 * Run-state allocation (all host-side; NPU does not hold per-token state)
 * ================================================================ */

static void alloc_state_cpu(Model *m) {
    Config *c = &m->cfg;
    State *s = &m->s;
    int kv_cache_len = c->n_layers * c->max_seq * c->kv_dim;

    size_t q_nelem  = (size_t)c->n_heads    * c->head_dim;
    size_t kv_nelem = (size_t)c->n_kv_heads * c->head_dim;
    size_t big_dim  = (size_t)c->dim > q_nelem ? (size_t)c->dim : q_nelem;

    s->x      = (float *)aligned_alloc(64, ((big_dim * sizeof(float)) + 63) & ~(size_t)63);
    s->xb     = (float *)aligned_alloc(64, ((big_dim * sizeof(float)) + 63) & ~(size_t)63);
    s->xb2    = (float *)aligned_alloc(64, ((big_dim * sizeof(float)) + 63) & ~(size_t)63);
    s->hb     = (float *)aligned_alloc(64, (((size_t)c->hidden_dim * sizeof(float)) + 63) & ~(size_t)63);
    s->hb2    = (float *)aligned_alloc(64, (((size_t)c->hidden_dim * sizeof(float)) + 63) & ~(size_t)63);
    s->q      = (float *)aligned_alloc(64, ((q_nelem  * sizeof(float)) + 63) & ~(size_t)63);
    s->k      = (float *)aligned_alloc(64, ((kv_nelem * sizeof(float)) + 63) & ~(size_t)63);
    s->v      = (float *)aligned_alloc(64, ((kv_nelem * sizeof(float)) + 63) & ~(size_t)63);
    s->att    = (float *)aligned_alloc(64, (((size_t)c->n_heads * c->max_seq * sizeof(float)) + 63) & ~(size_t)63);
    s->logits = (float *)aligned_alloc(64, (((size_t)c->vocab_size * sizeof(float)) + 63) & ~(size_t)63);
    s->kc     = (float *)aligned_alloc(64, (((size_t)kv_cache_len * sizeof(float)) + 63) & ~(size_t)63);
    s->vc     = (float *)aligned_alloc(64, (((size_t)kv_cache_len * sizeof(float)) + 63) & ~(size_t)63);

    s->x_bf16  = (uint16_t *)aligned_alloc(64, (((size_t)c->dim        * sizeof(uint16_t)) + 63) & ~(size_t)63);
    s->xb_bf16 = (uint16_t *)aligned_alloc(64, ((big_dim * sizeof(uint16_t)) + 63) & ~(size_t)63);
    s->hb_bf16 = (uint16_t *)aligned_alloc(64, (((size_t)c->hidden_dim * sizeof(uint16_t)) + 63) & ~(size_t)63);

    if (!s->x || !s->xb || !s->xb2 || !s->hb || !s->hb2 || !s->q || !s->k || !s->v ||
        !s->att || !s->logits || !s->kc || !s->vc ||
        !s->x_bf16 || !s->xb_bf16 || !s->hb_bf16)
        XDNA_DIE("failed to allocate run-time state buffers");
}

static void free_state_cpu(Model *m) {
    State *s = &m->s;
    free(s->x); free(s->xb); free(s->xb2);
    free(s->hb); free(s->hb2);
    free(s->q); free(s->k); free(s->v);
    free(s->att); free(s->logits);
    free(s->kc); free(s->vc);
    free(s->x_bf16); free(s->xb_bf16); free(s->hb_bf16);
    memset(s, 0, sizeof(*s));
}

/* ================================================================
 * XDNA2 NPU device layer — direct DRM ioctl
 *
 *   This is the bare-metal interface to drivers/accel/amdxdna. It does
 *   not depend on XRT (xrt-core / xrt-shim) or any third-party userspace
 *   driver — every call goes straight to the kernel via ioctl().
 *
 *   The set of ioctls we exercise is the strict minimum to:
 *     - probe the AIE topology and firmware version
 *     - create a hardware context bound to a subset of AIE columns
 *     - allocate buffer objects (SHMEM for host-shared, DEV_HEAP for the
 *       per-context instruction arena, CMD for the ert_packet itself)
 *     - submit an ERT_START_NPU command and wait for completion via the
 *       hwctx syncobj.
 * ================================================================ */

/* Prefer /dev/accel/accel*; fallback renderDn. Saves errno from last failure. */
static int open_amdxdna_drm_fd(int *last_errno_out) {
    char path[64];
    int last_err = ENOENT;

    for (int i = 0; i < 8; i++) {
        snprintf(path, sizeof(path), "/dev/accel/accel%d", i);
        int acc_fd = open(path, O_RDWR | O_CLOEXEC);
        if (acc_fd >= 0) {
            if (last_errno_out) *last_errno_out = 0;
            return acc_fd;
        }
        last_err = errno;

        snprintf(path, sizeof(path), "/dev/dri/renderD%d", 128 + i);
        acc_fd = open(path, O_RDWR | O_CLOEXEC);
        if (acc_fd >= 0) {
            if (last_errno_out) *last_errno_out = 0;
            return acc_fd;
        }
        last_err = errno;
    }
    if (last_errno_out) *last_errno_out = last_err;
    return -1;
}

static int npu_get_info(XdnaDev *dev, uint32_t param, void *buf, uint32_t sz) {
    struct amdxdna_drm_get_info g;
    memset(&g, 0, sizeof(g));
    g.param       = param;
    g.buffer_size = sz;
    g.buffer      = (uint64_t)(uintptr_t)buf;
    return ioctl(dev->fd, DRM_IOCTL_AMDXDNA_GET_INFO, &g);
}

static int npu_query_topology(XdnaDev *dev) {
    struct amdxdna_drm_query_aie_metadata md;
    memset(&md, 0, sizeof(md));
    if (npu_get_info(dev, DRM_AMDXDNA_QUERY_AIE_METADATA, &md, sizeof(md)) < 0)
        return -1;
    dev->aie_cols           = md.cols;
    dev->aie_rows           = md.rows;
    dev->aie_core_row_count = md.core.row_count;
    dev->aie_version_major = md.version.major;
    dev->aie_version_minor = md.version.minor;

    struct amdxdna_drm_query_firmware_version fw;
    memset(&fw, 0, sizeof(fw));
    if (npu_get_info(dev, DRM_AMDXDNA_QUERY_FIRMWARE_VERSION, &fw, sizeof(fw)) >= 0) {
        dev->fw_major = fw.major; dev->fw_minor = fw.minor;
        dev->fw_patch = fw.patch; dev->fw_build = fw.build;
    }

    struct amdxdna_drm_query_clock_metadata clk;
    memset(&clk, 0, sizeof(clk));
    if (npu_get_info(dev, DRM_AMDXDNA_QUERY_CLOCK_METADATA, &clk, sizeof(clk)) >= 0) {
        dev->h_clock_mhz   = clk.h_clock.freq_mhz;
        dev->npu_clock_mhz = clk.mp_npu_clock.freq_mhz;
    }
    return 0;
}

static void npu_create_bo(XdnaDev *dev, XdnaBo *bo, uint32_t type, size_t size) {
    memset(bo, 0, sizeof(*bo));
    if (dev->fd < 0) {
        /* CPU fallback: pretend we have a BO by allocating a page-aligned
         * buffer and stashing the pointer in `mapped`. The handle stays 0
         * so destroy_bo can tell which mode we're in. */
        size_t aligned = (size + 4095) & ~(size_t)4095;
        bo->size   = aligned;
        bo->type   = type;
        bo->mapped = aligned_alloc(4096, aligned);
        if (!bo->mapped) XDNA_DIE("aligned_alloc(%zu) failed", aligned);
        memset(bo->mapped, 0, aligned);
        bo->xdna_addr = (uint64_t)(uintptr_t)bo->mapped;
        return;
    }
    struct amdxdna_drm_create_bo c;
    memset(&c, 0, sizeof(c));
    c.size = size;
    c.type = type;
    XDNACHK(ioctl(dev->fd, DRM_IOCTL_AMDXDNA_CREATE_BO, &c));
    bo->handle = c.handle;
    bo->size   = size;
    bo->type   = type;

    struct amdxdna_drm_get_bo_info gi;
    memset(&gi, 0, sizeof(gi));
    gi.handle = bo->handle;
    XDNACHK(ioctl(dev->fd, DRM_IOCTL_AMDXDNA_GET_BO_INFO, &gi));
    bo->map_offset = gi.map_offset;
    bo->xdna_addr  = gi.xdna_addr;

    if (gi.vaddr) {
        bo->mapped = (void *)(uintptr_t)gi.vaddr;
    } else if (type != AMDXDNA_BO_DEV) {
        void *p = mmap(NULL, size, PROT_READ | PROT_WRITE, MAP_SHARED,
                       dev->fd, (off_t)bo->map_offset);
        if (p == MAP_FAILED)
            XDNA_DIE("mmap(BO handle=%u size=%zu offset=0x%llx) failed: %s",
                     bo->handle, size,
                     (unsigned long long)bo->map_offset, strerror(errno));
        bo->mapped = p;
    }
}

static void npu_destroy_bo(XdnaDev *dev, XdnaBo *bo) {
    if (!bo) return;
    if (dev->fd < 0 || bo->handle == 0) {
        if (bo->mapped) free(bo->mapped);
        memset(bo, 0, sizeof(*bo));
        return;
    }
    if (bo->mapped && bo->mapped != (void *)(uintptr_t)bo->xdna_addr)
        munmap(bo->mapped, bo->size);
    struct drm_gem_close gc;
    memset(&gc, 0, sizeof(gc));
    gc.handle = bo->handle;
    (void)ioctl(dev->fd, DRM_IOCTL_GEM_CLOSE, &gc);
    memset(bo, 0, sizeof(*bo));
}

static void npu_sync_bo(XdnaDev *dev, XdnaBo *bo, uint32_t direction) {
    if (dev->fd < 0 || !bo->handle) return;
    struct amdxdna_drm_sync_bo s;
    memset(&s, 0, sizeof(s));
    s.handle    = bo->handle;
    s.direction = direction;
    s.offset    = 0;
    s.size      = bo->size;
    XDNACHK(ioctl(dev->fd, DRM_IOCTL_AMDXDNA_SYNC_BO, &s));
}

/* Reserve `n` bytes of the per-context Instruction Buffer (a 64 MiB DEV_HEAP
 * BO allocated when the hwctx is created) and return a host pointer + xdna
 * virtual address for that region. Used to stage ctrlcode binaries. */
static int npu_insn_alloc(XdnaDev *dev, size_t n, void **host_out, uint64_t *xdna_out) {
    n = (n + 63) & ~(size_t)63;
    if (dev->insn_arena_used + n > dev->insn_heap.size) return -1;
    *host_out = (uint8_t *)dev->insn_heap.mapped + dev->insn_arena_used;
    *xdna_out = dev->insn_heap.xdna_addr + dev->insn_arena_used;
    dev->insn_arena_used += n;
    return 0;
}

static void npu_open(XdnaDev *dev) {
    memset(dev, 0, sizeof(*dev));
    dev->fd = -1;
    dev->gemv_dir = getenv("XDNA_GEMV_DIR");
    dev->force_cpu = getenv("XDNA_FORCE_CPU") ? 1 : 0;

    if (dev->force_cpu) {
        printf("XDNA: XDNA_FORCE_CPU set; staying in CPU fallback path\n");
        return;
    }

    int open_errno = 0;
    int fd = open_amdxdna_drm_fd(&open_errno);
    if (fd < 0) {
        printf("XDNA: cannot open accel/render DRM node (%s); using CPU fallback\n",
               strerror(open_errno));
        if (open_errno == EACCES)
            fprintf(stderr,
                    "XDNA: hint: permission denied — /dev/accel/accel0 is often "
                    "root:render 0660; run: sudo usermod -aG render %s && newgrp render\n",
                    getenv("USER") ? getenv("USER") : "$USER");
        else if (open_errno == ENOENT)
            fprintf(stderr,
                    "XDNA: hint: missing nodes — load amdxdna and install "
                    "/dev/accel/accel0\n");
        return;
    }
    dev->fd = fd;

    if (npu_query_topology(dev) < 0) {
        printf("XDNA: AIE topology query failed; not an amdxdna device; using CPU fallback\n");
        close(dev->fd);
        dev->fd = -1;
        return;
    }

    dev->have_device = 1;

    /* Allocate per-context instruction buffer (64 MiB DEV_HEAP).  This is the
     * arena into which we stage ctrlcode blobs and arg lists referenced by
     * ERT_START_NPU packets. */
    npu_create_bo(dev, &dev->insn_heap, AMDXDNA_BO_DEV_HEAP, 64UL * 1024 * 1024);
    npu_create_bo(dev, &dev->umq_bo,    AMDXDNA_BO_SHMEM,    64UL * 1024);
    npu_create_bo(dev, &dev->log_bo,    AMDXDNA_BO_SHMEM,    64UL * 1024);
    npu_create_bo(dev, &dev->cmd_bo,    AMDXDNA_BO_CMD,      4096);

    /* Create the hwctx bound to available AIE columns (driver may reject full md.cols). */
    struct amdxdna_qos_info qos;
    memset(&qos, 0, sizeof(qos));
    qos.gops              = 30000;
    qos.fps               = 1;
    qos.dma_bandwidth     = 0;
    qos.latency           = 0;
    qos.frame_exec_time   = 0;
    qos.priority          = 0;

    uint32_t nrow = dev->aie_core_row_count ? dev->aie_core_row_count : dev->aie_rows;
    if (!nrow) nrow = 1;

    uint32_t max_ncol = dev->aie_cols ? dev->aie_cols : 32;
    const char *xncol = getenv("XDNA_NUM_COL");
    if (xncol && atoi(xncol) > 0)
        max_ncol = (uint32_t)atoi(xncol);

    struct amdxdna_drm_create_hwctx h;
    int last_errno = EINVAL;
    uint32_t ncol_ok = 0;

    for (int ncol = (int)max_ncol; ncol >= 1; ncol--) {
        memset(&h, 0, sizeof(h));
        h.ext             = 0;
        h.ext_flags       = 0;
        h.qos_p           = (uint64_t)(uintptr_t)&qos;
        h.umq_bo          = dev->umq_bo.handle;
        h.log_buf_bo      = dev->log_bo.handle;
        h.max_opc         = 0x800;
        h.num_tiles       = (uint32_t)ncol * nrow;
        h.mem_size        = 0;

        if (ioctl(dev->fd, DRM_IOCTL_AMDXDNA_CREATE_HWCTX, &h) == 0) {
            dev->hwctx_handle   = h.handle;
            dev->syncobj_handle = h.syncobj_handle;
            ncol_ok = (uint32_t)ncol;
            break;
        }
        last_errno = errno;
    }

    if (!ncol_ok) {
        fprintf(stderr,
                "XDNA: CREATE_HWCTX failed ncol=1..%u nrow=%u errno=%d %s\n",
                max_ncol, nrow, last_errno, strerror(last_errno));
        fprintf(stderr,
                "XDNA: try XDNA_NUM_COL=1 or check dmesg (Invalid num_col / col list)\n");
        npu_destroy_bo(dev, &dev->cmd_bo);
        npu_destroy_bo(dev, &dev->log_bo);
        npu_destroy_bo(dev, &dev->umq_bo);
        npu_destroy_bo(dev, &dev->insn_heap);
        close(dev->fd);
        dev->fd = -1;
        dev->have_device = 0;
        return;
    }

    printf("XDNA: hwctx handle=%u syncobj=%u num_col=%u num_tiles=%u nrow=%u doorbell=0x%x | md cols=%u rows=%u core.row_count=%u\n",
           dev->hwctx_handle, dev->syncobj_handle,
           ncol_ok, ncol_ok * nrow, nrow, h.umq_doorbell,
           dev->aie_cols, dev->aie_rows, dev->aie_core_row_count);
}

static void npu_close(XdnaDev *dev) {
    if (dev->fd < 0) return;
    if (dev->hwctx_handle) {
        struct amdxdna_drm_destroy_hwctx d;
        memset(&d, 0, sizeof(d));
        d.handle = dev->hwctx_handle;
        (void)ioctl(dev->fd, DRM_IOCTL_AMDXDNA_DESTROY_HWCTX, &d);
    }
    npu_destroy_bo(dev, &dev->insn_heap);
    npu_destroy_bo(dev, &dev->cmd_bo);
    npu_destroy_bo(dev, &dev->log_bo);
    npu_destroy_bo(dev, &dev->umq_bo);
    close(dev->fd);
    dev->fd = -1;
}

/* Wait until the EXEC_CMD identified by `seq` has completed.
 * Uses DRM_SYNCOBJ_WAIT on the per-hwctx timeline syncobj. */
static int npu_wait(XdnaDev *dev, uint64_t seq, uint64_t timeout_ns) {
    if (dev->fd < 0) return 0;
    struct drm_syncobj_timeline_wait w;
    memset(&w, 0, sizeof(w));
    uint32_t handle = dev->syncobj_handle;
    uint64_t point  = seq;
    w.handles    = (uint64_t)(uintptr_t)&handle;
    w.points     = (uint64_t)(uintptr_t)&point;
    w.count_handles = 1;
    w.flags = DRM_SYNCOBJ_WAIT_FLAGS_WAIT_ALL;
    /* timeout is absolute CLOCK_MONOTONIC nanoseconds. */
    struct timespec tspec;
    clock_gettime(CLOCK_MONOTONIC, &tspec);
    uint64_t abs_ns = (uint64_t)tspec.tv_sec * 1000000000ULL + tspec.tv_nsec + timeout_ns;
    w.timeout_nsec = (int64_t)abs_ns;
    int r = ioctl(dev->fd, DRM_IOCTL_SYNCOBJ_TIMELINE_WAIT, &w);
    if (r < 0) {
        fprintf(stderr, "xdna: syncobj wait seq=%llu failed: %s\n",
                (unsigned long long)seq, strerror(errno));
        return -1;
    }
    return 0;
}

/* Build a single ERT_START_NPU packet in `dev->cmd_bo` referencing an
 * instruction buffer chunk and a small arg-list (xdna addresses of input,
 * output, weight buffers). Returns the sequence number assigned by the
 * EXEC_CMD ioctl. */
static int npu_submit_start_npu(XdnaDev *dev,
                                uint64_t insn_xdna, uint32_t insn_size,
                                const uint64_t *args, uint32_t arg_count,
                                uint64_t *out_seq) {
    if (dev->fd < 0 || !dev->cmd_bo.mapped) return -1;

    uint32_t *cmd = (uint32_t *)dev->cmd_bo.mapped;
    /* Header word: opcode = ERT_START_NPU, state = NEW, count = words after
     * header.  count covers: 1 cu_mask + amdxdna_cmd_start_npu + props. */
    uint32_t cu_mask = 0x1;
    uint32_t prop_words = arg_count * 2; /* each arg is a u64 = 2 u32 */
    /* Layout in words after header:
     *   [0]  cu_mask
     *   [1,2] buffer (u64 lo, hi)
     *   [3]  buffer_size
     *   [4]  prop_count
     *   [5..] prop_args (each u64 = 2 u32)
     */
    uint32_t count = 1 + 4 + prop_words;

    struct ert_packet_header hdr;
    hdr.header = 0;
    hdr.state  = ERT_CMD_STATE_NEW;
    hdr.count  = count;
    hdr.opcode = ERT_START_NPU & 0x1F;
    hdr.type   = 0;
    cmd[0] = hdr.header;
    cmd[1] = cu_mask;
    cmd[2] = (uint32_t)(insn_xdna & 0xFFFFFFFFu);
    cmd[3] = (uint32_t)(insn_xdna >> 32);
    cmd[4] = insn_size;
    cmd[5] = arg_count;
    for (uint32_t i = 0; i < arg_count; i++) {
        cmd[6 + i*2 + 0] = (uint32_t)(args[i] & 0xFFFFFFFFu);
        cmd[6 + i*2 + 1] = (uint32_t)(args[i] >> 32);
    }

    npu_sync_bo(dev, &dev->cmd_bo, SYNC_DIRECT_TO_DEVICE);

    uint32_t cmd_handle = dev->cmd_bo.handle;
    struct amdxdna_drm_exec_cmd ex;
    memset(&ex, 0, sizeof(ex));
    ex.hwctx       = dev->hwctx_handle;
    ex.type        = AMDXDNA_CMD_SUBMIT_EXEC_BUF;
    ex.cmd_handles = (uint64_t)(uintptr_t)&cmd_handle;
    ex.cmd_count   = 1;
    ex.args        = 0;
    ex.arg_count   = 0;
    if (ioctl(dev->fd, DRM_IOCTL_AMDXDNA_EXEC_CMD, &ex) < 0) {
        fprintf(stderr, "xdna: EXEC_CMD failed: %s\n", strerror(errno));
        return -1;
    }
    *out_seq = ex.seq;
    dev->last_seq = ex.seq;
    return 0;
}

/* ================================================================
 * Precompiled GEMV ctrlcode resolution
 *
 *   We look for a file at $XDNA_GEMV_DIR/bf16-gemv-<n>x<d>.bin. The file
 *   format is the raw ctrlcode (XAie_TxnOpcode sequence) generated by the
 *   AMD IRON / MLIR-AIE toolchain for that exact (n,d) GEMV shape. If the
 *   file is missing we fall back to CPU.
 * ================================================================ */

typedef struct {
    int      have;
    void    *insn_host;
    uint64_t insn_xdna;
    uint32_t insn_size;
} GemvKernel;

static int load_gemv_kernel(XdnaDev *dev, int n, int d, GemvKernel *out) {
    memset(out, 0, sizeof(*out));
    if (dev->fd < 0 || !dev->gemv_dir) return -1;

    char path[512];
    snprintf(path, sizeof(path), "%s/bf16-gemv-%dx%d.bin", dev->gemv_dir, n, d);
    int fd = open(path, O_RDONLY);
    if (fd < 0) return -1;
    struct stat st;
    if (fstat(fd, &st) < 0) { close(fd); return -1; }
    size_t sz = (size_t)st.st_size;

    void *host;
    uint64_t xdna;
    if (npu_insn_alloc(dev, sz, &host, &xdna) < 0) {
        close(fd);
        return -1;
    }
    ssize_t r = read(fd, host, sz);
    close(fd);
    if (r != (ssize_t)sz) return -1;

    out->have      = 1;
    out->insn_host = host;
    out->insn_xdna = xdna;
    out->insn_size = (uint32_t)sz;
    return 0;
}

/* ================================================================
 * BF16 GEMV (CPU fallback) and BF16 embedding lookup.
 *
 *   The CPU path computes the same dot product the NPU would, in FP32
 *   accumulators using BF16 weights and FP32 inputs. With OpenMP one
 *   thread handles one output row, mirroring the per-row parallelism of
 *   the original ROCm GEMV kernel where one warp handled one row.
 * ================================================================ */

/* Dequantize a full row-major GEMV matrix from mmap'd GGUF weights into the
 * model's single BF16 scratch BO (+ FP32 staging in scratch_f32). */
static void weight_prepare_bf16(Model *m, const void *raw, int type, size_t nel) {
    WeightsDev *wd = &m->wd;
    XdnaBo *bo = &wd->w_scratch_bo;

    if (!bo->mapped) XDNA_DIE("weight scratch BO not initialized");
    if (!wd->scratch_f32) XDNA_DIE("scratch_f32 unset");
    if (nel > SIZE_MAX / sizeof(uint16_t) || nel * sizeof(uint16_t) > bo->size)
        XDNA_DIE("weight nel=%zu exceeds scratch (%zu bytes)", nel, (size_t)bo->size);

    float *f32 = wd->scratch_f32;

    if (type == DT_F32) {
        memcpy(f32, raw, nel * sizeof(float));
    } else if (type == DT_F16) {
        const uint16_t *r = (const uint16_t *)raw;
        for (size_t i = 0; i < nel; i++) f32[i] = host_f16f32(r[i]);
    } else {
        if (nel % QK_K) XDNA_DIE("quant weight nel %% QK_K != 0 (nel=%zu)", nel);
        size_t nb = nel / QK_K;
        switch (type) {
        case DT_Q4_K:  dequant_q4_k ((const BlockQ4_K *)raw,  f32, nb); break;
        case DT_Q5_K:  dequant_q5_k ((const BlockQ5_K *)raw,  f32, nb); break;
        case DT_IQ2_S: dequant_iq2_s((const BlockIQ2_S *)raw, f32, nb); break;
        case DT_IQ3_S: dequant_iq3_s((const BlockIQ3_S *)raw, f32, nb); break;
        default:
            XDNA_DIE("unsupported GEMV weight dtype %d", type);
        }
    }

    uint16_t *dst = (uint16_t *)bo->mapped;
    for (size_t i = 0; i < nel; i++) dst[i] = host_f32bf16(f32[i]);

    npu_sync_bo(&m->npu, bo, SYNC_DIRECT_TO_DEVICE);
}

static void cpu_bf16_gemv(float *o, const float *x, const uint16_t *w,
                          int n, int d) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int i = 0; i < d; i++) {
        const uint16_t *row = w + (size_t)i * n;
        /* Manual 8x unroll for some autovec friendliness. */
        float acc0 = 0.f, acc1 = 0.f, acc2 = 0.f, acc3 = 0.f;
        int j = 0;
        for (; j + 7 < n; j += 8) {
            acc0 += x[j+0] * host_bf16f32(row[j+0]);
            acc1 += x[j+1] * host_bf16f32(row[j+1]);
            acc2 += x[j+2] * host_bf16f32(row[j+2]);
            acc3 += x[j+3] * host_bf16f32(row[j+3]);
            acc0 += x[j+4] * host_bf16f32(row[j+4]);
            acc1 += x[j+5] * host_bf16f32(row[j+5]);
            acc2 += x[j+6] * host_bf16f32(row[j+6]);
            acc3 += x[j+7] * host_bf16f32(row[j+7]);
        }
        float acc = acc0 + acc1 + acc2 + acc3;
        for (; j < n; j++) acc += x[j] * host_bf16f32(row[j]);
        o[i] = acc;
    }
}

static void f32_to_bf16(const float *src, uint16_t *dst, int n) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int i = 0; i < n; i++) dst[i] = host_f32bf16(src[i]);
}

/* Dispatch a BF16 GEMV. Quantized mmap weights → scratch BO, then tries NPU
 * (if available + kernel exists), else OpenMP CPU BF16 GEMV. */
static void launch_mm_bf16(Model *m, float *o, const float *x,
                           const void *w_raw, int w_type, int n, int d) {
    size_t nel = (size_t)n * (size_t)d;
    weight_prepare_bf16(m, w_raw, w_type, nel);

    XdnaDev *dev = &m->npu;
    const XdnaBo *w_bo = &m->wd.w_scratch_bo;
    const uint16_t *w_host = (const uint16_t *)w_bo->mapped;

    if (dev->have_device && !dev->force_cpu) {
        GemvKernel k;
        if (load_gemv_kernel(dev, n, d, &k) == 0) {
            /* Stage input as BF16. We reuse the per-model BF16 staging buffer
             * for x; if the activation is larger than `dim` (e.g. xb is
             * passed through wo with n=qdim), then x_bf16 may be too small
             * — use xb_bf16 (which is sized for the largest single vector). */
            uint16_t *xbf = m->s.xb_bf16;
            f32_to_bf16(x, xbf, n);

            /* Build an input BO around xbf using DEV_HEAP arena (cheap) or
             * an ephemeral SHMEM. Since the activation is small, we allocate
             * a fresh SHMEM BO once per call here — a production runtime
             * would recycle these. */
            XdnaBo in_bo, out_bo;
            npu_create_bo(dev, &in_bo,  AMDXDNA_BO_SHMEM, (size_t)n * sizeof(uint16_t));
            npu_create_bo(dev, &out_bo, AMDXDNA_BO_SHMEM, (size_t)d * sizeof(float));
            memcpy(in_bo.mapped, xbf, (size_t)n * sizeof(uint16_t));
            npu_sync_bo(dev, &in_bo, SYNC_DIRECT_TO_DEVICE);

            uint64_t args[3] = {
                in_bo.xdna_addr,
                w_bo->xdna_addr,
                out_bo.xdna_addr,
            };
            uint64_t seq = 0;
            if (npu_submit_start_npu(dev, k.insn_xdna, k.insn_size,
                                     args, 3, &seq) == 0 &&
                npu_wait(dev, seq, 5ULL * 1000 * 1000 * 1000) == 0) {
                npu_sync_bo(dev, &out_bo, SYNC_DIRECT_FROM_DEVICE);
                memcpy(o, out_bo.mapped, (size_t)d * sizeof(float));
                npu_destroy_bo(dev, &in_bo);
                npu_destroy_bo(dev, &out_bo);
                dev->npu_gemv_calls++;
                return;
            }
            npu_destroy_bo(dev, &in_bo);
            npu_destroy_bo(dev, &out_bo);
        }
    }
    cpu_bf16_gemv(o, x, w_host, n, d);
    dev->cpu_gemv_calls++;
}

/* ================================================================
 * CPU activation kernels (RMSNorm, RoPE, attention, SiLU, residuals)
 *
 *   These mirror the OpenMP-parallel implementations in main-omp.c, using
 *   the same per-row / per-head parallelism granularity as the ROCm CUDA
 *   kernels so the dataflow and dispatching ordering match the GPU build.
 * ================================================================ */

static void launch_rmsnorm(float *o, const float *x, const float *w, int n, float eps) {
    float ss = 0.f;
#ifdef _OPENMP
    #pragma omp parallel for reduction(+:ss) schedule(static)
#endif
    for (int i = 0; i < n; i++) ss += x[i] * x[i];
    ss = 1.0f / sqrtf(ss / (float)n + eps);
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int i = 0; i < n; i++) o[i] = x[i] * ss * w[i];
}

static void launch_rmsnorm_head(float *vec, const float *w, int n_heads, int hd, float eps) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int h = 0; h < n_heads; h++) {
        float *seg = vec + (size_t)h * hd;
        float ss = 0.f;
        for (int i = 0; i < hd; i++) ss += seg[i] * seg[i];
        ss = 1.0f / sqrtf(ss / (float)hd + eps);
        for (int i = 0; i < hd; i++) seg[i] *= ss * w[i];
    }
}

static void launch_rope(float *vec, int n_heads, int hd, int pos, float theta_base) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int h = 0; h < n_heads; h++) {
        for (int i = 0; i < hd; i += 2) {
            float freq  = 1.0f / powf(theta_base, (float)i / (float)hd);
            float angle = (float)pos * freq;
            float cr = cosf(angle), ci = sinf(angle);
            int idx = h * hd + i;
            float v0 = vec[idx], v1 = vec[idx + 1];
            vec[idx]     = v0 * cr - v1 * ci;
            vec[idx + 1] = v0 * ci + v1 * cr;
        }
    }
}

static void launch_kv_write(const float *k, const float *v,
                            float *kc_pos, float *vc_pos, int kv_dim) {
    memcpy(kc_pos, k, (size_t)kv_dim * sizeof(float));
    memcpy(vc_pos, v, (size_t)kv_dim * sizeof(float));
}

static void launch_silu_mul(float *hb, const float *hb2, int n) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int i = 0; i < n; i++) {
        float v = hb[i];
        v = v / (1.0f + expf(-v));
        hb[i] = v * hb2[i];
    }
}

static void launch_vec_add(float *x, const float *y, int n) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int i = 0; i < n; i++) x[i] += y[i];
}

static void softmax_inplace(float *x, int n) {
    float mx = x[0];
    for (int i = 1; i < n; i++) if (x[i] > mx) mx = x[i];
    float sum = 0.f;
    for (int i = 0; i < n; i++) {
        x[i] = expf(x[i] - mx);
        sum += x[i];
    }
    float inv = 1.f / sum;
    for (int i = 0; i < n; i++) x[i] *= inv;
}

static void launch_attn(float *xb, const float *q,
                        const float *kc, const float *vc, float *att,
                        size_t loff, int pos, int n_heads, int kv_mul,
                        int hd, int kv_dim, int max_seq) {
#ifdef _OPENMP
    #pragma omp parallel for schedule(static)
#endif
    for (int h = 0; h < n_heads; h++) {
        const float *qh = q + (size_t)h * hd;
        int kvh = h / kv_mul;
        float *att_h = att + (size_t)h * max_seq;
        float sc = 1.0f / sqrtf((float)hd);

        for (int t = 0; t <= pos; t++) {
            const float *kt = kc + loff + (size_t)t * kv_dim + (size_t)kvh * hd;
            float score = 0.f;
            for (int i = 0; i < hd; i++) score += qh[i] * kt[i];
            att_h[t] = score * sc;
        }
        softmax_inplace(att_h, pos + 1);

        float *oh = xb + (size_t)h * hd;
        memset(oh, 0, (size_t)hd * sizeof(float));
        for (int t = 0; t <= pos; t++) {
            float a = att_h[t];
            const float *vt = vc + loff + (size_t)t * kv_dim + (size_t)kvh * hd;
            for (int i = 0; i < hd; i++) oh[i] += a * vt[i];
        }
    }
}

/* ================================================================
 * Forward pass (single token at position `pos`, hybrid CPU + NPU)
 * ================================================================ */

static void forward_xdna(Model *m, int token, int pos, int need_logits) {
    Config *c = &m->cfg;
    WeightsDev *wd = &m->wd;
    State *s = &m->s;
    int dim      = c->dim;
    int hd       = c->head_dim;
    int kv_dim   = c->kv_dim;
    int kv_mul   = c->kv_mul;
    int n_heads  = c->n_heads;
    int n_kv     = c->n_kv_heads;
    int max_seq  = c->max_seq;
    int hidden   = c->hidden_dim;
    int qdim     = n_heads * hd;

    emb_lookup_xdna(s->x, wd->embd, wd->embd_t, token, dim);

    for (int l = 0; l < c->n_layers; l++) {
        launch_rmsnorm(s->xb, s->x, wd->norm_att[l], dim, c->norm_eps);

        launch_mm_bf16(m, s->q, s->xb, wd->wq[l], wd->wq_t[l], dim, qdim);
        launch_mm_bf16(m, s->k, s->xb, wd->wk[l], wd->wk_t[l], dim, kv_dim);
        launch_mm_bf16(m, s->v, s->xb, wd->wv[l], wd->wv_t[l], dim, kv_dim);

        launch_rmsnorm_head(s->q, wd->q_norm[l], n_heads, hd, c->norm_eps);
        launch_rmsnorm_head(s->k, wd->k_norm[l], n_kv,    hd, c->norm_eps);

        launch_rope(s->q, n_heads, hd, pos, c->rope_theta);
        launch_rope(s->k, n_kv,    hd, pos, c->rope_theta);

        size_t loff   = (size_t)l * max_seq * kv_dim;
        float *kc_pos = s->kc + loff + (size_t)pos * kv_dim;
        float *vc_pos = s->vc + loff + (size_t)pos * kv_dim;
        launch_kv_write(s->k, s->v, kc_pos, vc_pos, kv_dim);

        launch_attn(s->xb, s->q, s->kc, s->vc, s->att,
                    loff, pos, n_heads, kv_mul, hd, kv_dim, max_seq);

        launch_mm_bf16(m, s->xb2, s->xb, wd->wo[l], wd->wo_t[l], qdim, dim);
        launch_vec_add(s->x, s->xb2, dim);

        launch_rmsnorm(s->xb, s->x, wd->norm_ffn[l], dim, c->norm_eps);

        launch_mm_bf16(m, s->hb,  s->xb, wd->gate[l], wd->gate_t[l], dim, hidden);
        launch_mm_bf16(m, s->hb2, s->xb, wd->up[l],   wd->up_t[l],   dim, hidden);

        launch_silu_mul(s->hb, s->hb2, hidden);

        launch_mm_bf16(m, s->xb, s->hb, wd->down[l], wd->down_t[l], hidden, dim);
        launch_vec_add(s->x, s->xb, dim);
    }

    if (need_logits) {
        launch_rmsnorm(s->x, s->x, wd->norm_out, dim, c->norm_eps);
        launch_mm_bf16(m, s->logits, s->x, wd->out, wd->out_t, dim, c->vocab_size);
    }
}

/* ================================================================
 * Sampling (CPU; top-p nucleus fallback)
 * ================================================================ */

static void softmax_host(float *x, int n) {
    float max_val = x[0];
    for (int i = 1; i < n; i++) if (x[i] > max_val) max_val = x[i];
    float sum = 0.0f;
    for (int i = 0; i < n; i++) {
        x[i] = expf(x[i] - max_val);
        sum += x[i];
    }
    for (int i = 0; i < n; i++) x[i] /= sum;
}

static float rng_f32(uint64_t *state) {
    *state ^= *state >> 12;
    *state ^= *state << 25;
    *state ^= *state >> 27;
    return (float)((*state * 0x2545F4914F6CDD1DULL) >> 40) / (float)(1 << 24);
}

typedef struct { float p; int idx; } ProbIdx;

static int cmp_prob_desc(const void *a, const void *b) {
    float pa = ((const ProbIdx *)a)->p;
    float pb = ((const ProbIdx *)b)->p;
    return (pa < pb) - (pa > pb);
}

static int sample_token(float *logits, int n, float temp, float topp, uint64_t *rng) {
    if (temp <= 0.0f) {
        int best = 0;
        for (int i = 1; i < n; i++) if (logits[i] > logits[best]) best = i;
        return best;
    }

    for (int i = 0; i < n; i++) logits[i] /= temp;
    softmax_host(logits, n);

    float coin = rng_f32(rng);

    if (topp <= 0.0f || topp >= 1.0f) {
        float cdf = 0.0f;
        for (int i = 0; i < n; i++) {
            cdf += logits[i];
            if (coin < cdf) return i;
        }
        return n - 1;
    }

    ProbIdx *pi = (ProbIdx *)malloc(n * sizeof(ProbIdx));
    int np = 0;
    float cutoff = (1.0f - topp) / (n - 1);
    for (int i = 0; i < n; i++) {
        if (logits[i] >= cutoff) {
            pi[np].p = logits[i];
            pi[np].idx = i;
            np++;
        }
    }
    qsort(pi, np, sizeof(ProbIdx), cmp_prob_desc);

    float cum = 0.0f;
    int last = np - 1;
    for (int i = 0; i < np; i++) {
        cum += pi[i].p;
        if (cum > topp) { last = i; break; }
    }

    float r = rng_f32(rng) * cum;
    float cdf = 0.0f;
    int result = pi[last].idx;
    for (int i = 0; i <= last; i++) {
        cdf += pi[i].p;
        if (r < cdf) { result = pi[i].idx; break; }
    }
    free(pi);
    return result;
}

/* ================================================================
 * Token Printer
 * ================================================================ */

static int is_special(Tok *tk, int id) {
    return id == tk->bos || id == tk->eos || id == tk->eot ||
           id == tk->im_start || id == tk->im_end;
}

static void print_tok(Tok *tk, int id) {
    if (id < 0 || id >= tk->size || is_special(tk, id)) return;
    const uint8_t *s = (const uint8_t *)tk->vocab[id];
    int len = tk->vlen[id];
    for (int i = 0; i < len; ) {
        int cp, adv;
        if (s[i] < 0x80) { cp = s[i]; adv = 1; }
        else if ((s[i] & 0xE0) == 0xC0 && i + 1 < len) {
            cp = ((s[i] & 0x1F) << 6) | (s[i+1] & 0x3F); adv = 2;
        } else if ((s[i] & 0xF0) == 0xE0 && i + 2 < len) {
            cp = ((s[i] & 0x0F) << 12) | ((s[i+1] & 0x3F) << 6) | (s[i+2] & 0x3F); adv = 3;
        } else { cp = s[i]; adv = 1; }
        int raw = gpt2_codepoint_to_byte(cp);
        if (raw >= 0) putchar(raw);
        else          fwrite(s + i, 1, adv, stdout);
        i += adv;
    }
    fflush(stdout);
}

/* ================================================================
 * Text Generation
 * ================================================================ */

static void generate(Model *m, int *prompt, int n_prompt,
                     int max_new, float temp, float topp, uint64_t seed) {
    uint64_t rng = seed;
    if (rng == 0) rng = 1;

    int token = prompt[0];
    int gen = 0;

    struct timespec t0, t1;
    clock_gettime(CLOCK_MONOTONIC, &t0);

    for (int pos = 0; pos < n_prompt + max_new - 1; pos++) {
        if (pos >= m->cfg.max_seq) {
            fprintf(stderr, "\n[max sequence length %d reached]\n", m->cfg.max_seq);
            break;
        }

        int need_logits = (pos >= n_prompt - 1);
        forward_xdna(m, token, pos, need_logits);

        int next;
        if (pos < n_prompt - 1) {
            next = prompt[pos + 1];
        } else {
            next = sample_token(m->s.logits, m->cfg.vocab_size, temp, topp, &rng);
            if (next == m->tok.eos || next == m->tok.eot) break;
            gen++;
            print_tok(&m->tok, next);
        }
        token = next;
    }

    clock_gettime(CLOCK_MONOTONIC, &t1);
    double elapsed = (t1.tv_sec - t0.tv_sec) + (t1.tv_nsec - t0.tv_nsec) / 1e9;
    printf("\n\n--- %d prompt tokens + %d generated tokens ---\n", n_prompt, gen);
    printf("--- %.1fs total (%.2f tok/s) ---\n", elapsed,
           (gen > 0) ? gen / elapsed : 0.0);
    printf("--- XDNA: %llu NPU GEMV / %llu CPU GEMV submissions ---\n",
           (unsigned long long)m->npu.npu_gemv_calls,
           (unsigned long long)m->npu.cpu_gemv_calls);
}

/* ================================================================
 * Main
 * ================================================================ */

int main(int argc, char *argv[]) {
    if (argc < 2) {
        fprintf(stderr, "Usage: %s <model.gguf> [options]\n", argv[0]);
        fprintf(stderr, "Options:\n");
        fprintf(stderr, "  -p <prompt>   User prompt (default: Hello)\n");
        fprintf(stderr, "  -n <tokens>   Max tokens to generate (default: 256)\n");
        fprintf(stderr, "  -t <temp>     Temperature (default: 0.7)\n");
        fprintf(stderr, "  -k <topp>     Top-p sampling (default: 0.8)\n");
        fprintf(stderr, "  -s <seed>     Random seed (default: time)\n");
        fprintf(stderr, "  -l <len>      Max sequence length (default: 512)\n");
        return 1;
    }

    char *model_path = argv[1];
    const char *prompt = "Hello";
    int   max_tokens = 256;
    float temp       = 0.7f;
    float topp       = 0.8f;
    uint64_t seed    = (uint64_t)time(NULL);
    int   max_seq    = 512;

    for (int i = 2; i + 1 < argc; i += 2) {
        if      (!strcmp(argv[i], "-p")) prompt     = argv[i + 1];
        else if (!strcmp(argv[i], "-n")) max_tokens = atoi(argv[i + 1]);
        else if (!strcmp(argv[i], "-t")) temp       = (float)atof(argv[i + 1]);
        else if (!strcmp(argv[i], "-k")) topp       = (float)atof(argv[i + 1]);
        else if (!strcmp(argv[i], "-s")) seed       = (uint64_t)strtoull(argv[i + 1], NULL, 10);
        else if (!strcmp(argv[i], "-l")) max_seq    = atoi(argv[i + 1]);
    }

    printf("Loading %s ...\n", model_path);

    Model model;
    memset(&model, 0, sizeof(model));

    model.fd = open(model_path, O_RDONLY);
    if (model.fd < 0) {
        fprintf(stderr, "Error: cannot open %s\n", model_path);
        return 1;
    }
    struct stat st;
    fstat(model.fd, &st);
    model.fsz = (size_t)st.st_size;
    model.fdata = (uint8_t *)mmap(NULL, model.fsz, PROT_READ, MAP_PRIVATE, model.fd, 0);
    if (model.fdata == MAP_FAILED) {
        fprintf(stderr, "Error: mmap failed\n");
        return 1;
    }

    char **merges = NULL;
    int n_merges = 0;
    parse_gguf(&model, &merges, &n_merges);

    model.cfg.max_seq = max_seq;

    Config *c = &model.cfg;
    printf("Model: dim=%d hidden=%d layers=%d heads=%d kv_heads=%d vocab=%d\n",
           c->dim, c->hidden_dim, c->n_layers, c->n_heads, c->n_kv_heads, c->vocab_size);
    printf("       head_dim=%d kv_dim=%d kv_mul=%d rope_theta=%.0f eps=%.1e max_seq=%d\n",
           c->head_dim, c->kv_dim, c->kv_mul, c->rope_theta, c->norm_eps, c->max_seq);

    init_tokenizer(&model.tok, merges, n_merges);

    /* Open NPU first so GEMV scratch uses real SHMEM BOs when hardware exists. */
    npu_open(&model.npu);
    if (model.npu.have_device) {
        printf("XDNA2: NPU device opened (fd=%d)\n", model.npu.fd);
        printf("       AIE %ux%u (version %u.%u)  H_clock=%u MHz  NPU_clock=%u MHz\n",
               model.npu.aie_cols, model.npu.aie_rows,
               model.npu.aie_version_major, model.npu.aie_version_minor,
               model.npu.h_clock_mhz, model.npu.npu_clock_mhz);
        printf("       firmware %u.%u.%u (build %u)\n",
               model.npu.fw_major, model.npu.fw_minor,
               model.npu.fw_patch, model.npu.fw_build);
        if (model.npu.gemv_dir)
            printf("       GEMV ctrlcode directory: %s\n", model.npu.gemv_dir);
        else
            printf("       NB: XDNA_GEMV_DIR is unset — GEMV will run on CPU fallback\n");
    } else {
        printf("XDNA2: NPU device unavailable; running fully on CPU OpenMP fallback\n");
    }

    load_weights_xdna(&model);
    alloc_weight_scratch(&model);
    free_tensor_index(&model);
    alloc_state_cpu(&model);

    int n_prompt_tokens;
    int *prompt_tokens = chat_encode(&model.tok, prompt, &n_prompt_tokens);
    printf("Prompt: \"%s\" (%d tokens)\n\n", prompt, n_prompt_tokens);

    generate(&model, prompt_tokens, n_prompt_tokens, max_tokens, temp, topp, seed);

    free(prompt_tokens);
    free_state_cpu(&model);
    free_dev_weights(&model);
    npu_close(&model.npu);
    munmap(model.fdata, model.fsz);
    close(model.fd);

    return 0;
}
