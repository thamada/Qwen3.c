/* ================================================================
 * Binary Reader Utilities (GGUF metadata block)
 * ================================================================ */

typedef struct { uint8_t *d; uint64_t p; } Rd;

static uint32_t ru32(Rd *r) { uint32_t v; memcpy(&v, r->d + r->p, 4); r->p += 4; return v; }
static uint64_t ru64(Rd *r) { uint64_t v; memcpy(&v, r->d + r->p, 8); r->p += 8; return v; }
static float    rf32(Rd *r) { float    v; memcpy(&v, r->d + r->p, 4); r->p += 4; return v; }

static char *rstr(Rd *r, int *out_len) {
    uint64_t n = ru64(r);
    if (n > 0x7FFFFFFF) {
        fprintf(stderr, "Error: corrupt string length %llu\n", (unsigned long long)n);
        exit(1);
    }
    char *s = (char *)malloc(n + 1);
    memcpy(s, r->d + r->p, n);
    s[n] = 0;
    r->p += n;
    if (out_len) *out_len = (int)n;
    return s;
}

static void skip(Rd *r, uint32_t t) {
    switch (t) {
    case GV_U8: case GV_I8: case GV_BOOL: r->p++; break;
    case GV_U16: case GV_I16: r->p += 2; break;
    case GV_U32: case GV_I32: case GV_F32: r->p += 4; break;
    case GV_U64: case GV_I64: case GV_F64: r->p += 8; break;
    case GV_STR: { uint64_t n = ru64(r); r->p += n; break; }
    case GV_ARR: {
        uint32_t at = ru32(r); uint64_t al = ru64(r);
        for (uint64_t i = 0; i < al; i++) skip(r, at);
        break;
    }
    }
}

static int64_t read_int_val(Rd *r, uint32_t vt) {
    switch (vt) {
    case GV_U8: case GV_BOOL: { uint8_t v = r->d[r->p]; r->p++; return (int64_t)v; }
    case GV_I8:  { int8_t v; memcpy(&v, r->d + r->p, 1); r->p++; return (int64_t)v; }
    case GV_U16: { uint16_t v; memcpy(&v, r->d + r->p, 2); r->p += 2; return (int64_t)v; }
    case GV_I16: { int16_t v; memcpy(&v, r->d + r->p, 2); r->p += 2; return (int64_t)v; }
    case GV_U32: return (int64_t)ru32(r);
    case GV_I32: { int32_t v; memcpy(&v, r->d + r->p, 4); r->p += 4; return (int64_t)v; }
    case GV_U64: return (int64_t)ru64(r);
    case GV_I64: { int64_t v; memcpy(&v, r->d + r->p, 8); r->p += 8; return v; }
    case GV_F32: return (int64_t)rf32(r);
    case GV_F64: { double v; memcpy(&v, r->d + r->p, 8); r->p += 8; return (int64_t)v; }
    default: skip(r, vt); return 0;
    }
}

static float read_float_val(Rd *r, uint32_t vt) {
    switch (vt) {
    case GV_F32: return rf32(r);
    case GV_F64: { double v; memcpy(&v, r->d + r->p, 8); r->p += 8; return (float)v; }
    case GV_U32: return (float)ru32(r);
    case GV_I32: { int32_t v; memcpy(&v, r->d + r->p, 4); r->p += 4; return (float)v; }
    case GV_U64: return (float)ru64(r);
    case GV_I64: { int64_t v; memcpy(&v, r->d + r->p, 8); r->p += 8; return (float)v; }
    default: skip(r, vt); return 0.0f;
    }
}

/* ================================================================
 * Tokenizer hash table & GPT-2 byte encoding
 * ================================================================ */

static unsigned int hash_bytes(const char *s, int len) {
    unsigned int h = 2166136261u;
    for (int i = 0; i < len; i++) {
        h ^= (unsigned char)s[i];
        h *= 16777619u;
    }
    return h;
}

static void tok_build_hash(Tok *tk) {
    tk->htab_sz = tk->size * 2;
    tk->htab = (int *)malloc(tk->htab_sz * sizeof(int));
    for (int i = 0; i < tk->htab_sz; i++) tk->htab[i] = -1;
    for (int i = 0; i < tk->size; i++) {
        unsigned int h = hash_bytes(tk->vocab[i], tk->vlen[i]) % tk->htab_sz;
        while (tk->htab[h] != -1) h = (h + 1) % tk->htab_sz;
        tk->htab[h] = i;
    }
}

static int tok_lookup(Tok *tk, const char *s, int len) {
    unsigned int h = hash_bytes(s, len) % tk->htab_sz;
    while (tk->htab[h] != -1) {
        int id = tk->htab[h];
        if (tk->vlen[id] == len && memcmp(tk->vocab[id], s, len) == 0) return id;
        h = (h + 1) % tk->htab_sz;
    }
    return -1;
}

static int tok_find_special(Tok *tk, const char *name) {
    int nlen = (int)strlen(name);
    for (int i = tk->size - 1; i >= 0; i--) {
        if (tk->vlen[i] == nlen && memcmp(tk->vocab[i], name, nlen) == 0) return i;
    }
    return -1;
}

static int gpt2_is_printable(int b) {
    return (b >= 0x21 && b <= 0x7E) ||
           (b >= 0xA1 && b <= 0xAC) ||
           (b >= 0xAE && b <= 0xFF);
}

static int gpt2_byte_to_codepoint(int b) {
    if (gpt2_is_printable(b)) return b;
    int n = 0;
    for (int i = 0; i < b; i++)
        if (!gpt2_is_printable(i)) n++;
    return 256 + n;
}

static void build_byte_tokens(Tok *tk) {
    for (int b = 0; b < 256; b++) {
        int cp = gpt2_byte_to_codepoint(b);
        char utf8[4];
        int len;
        if (cp < 0x80) {
            utf8[0] = (char)cp;
            len = 1;
        } else {
            utf8[0] = (char)(0xC0 | (cp >> 6));
            utf8[1] = (char)(0x80 | (cp & 0x3F));
            len = 2;
        }
        tk->byte_tok[b] = tok_lookup(tk, utf8, len);
    }
}

static int gpt2_codepoint_to_byte(int cp) {
    if (gpt2_is_printable(cp)) return cp;
    if (cp >= 256 && cp < 324) {
        int n = cp - 256, count = 0;
        for (int b = 0; b < 256; b++) {
            if (!gpt2_is_printable(b)) {
                if (count == n) return b;
                count++;
            }
        }
    }
    return -1;
}

/* ================================================================
 * GGUF Parser (qwen3vl.* keys)
 * ================================================================ */

static void parse_gguf(Model *m, char ***out_merges, int *out_n_merges) {
    Rd r = { m->fdata, 0 };

    uint32_t magic = ru32(&r);
    if (magic != GGUF_MAGIC) {
        fprintf(stderr, "Error: invalid GGUF magic 0x%08x\n", magic);
        exit(1);
    }
    uint32_t ver = ru32(&r);
    if (ver < 2) {
        fprintf(stderr, "Error: GGUF v%u not supported (need v2+)\n", ver);
        exit(1);
    }
    uint64_t n_tensors = ru64(&r);
    uint64_t n_kv      = ru64(&r);

    printf("GGUF v%u | %llu tensors | %llu metadata entries\n",
           ver, (unsigned long long)n_tensors, (unsigned long long)n_kv);

    Config *c = &m->cfg;
    Tok *tk = &m->tok;

    /* sane defaults; Qwen3-VL-8B */
    c->rope_theta = 5000000.0f;
    c->norm_eps   = 1e-6f;
    c->max_seq    = 512;
    tk->bos = 151643;
    tk->eos = 151645;
    tk->eot = 151645;
    tk->im_start = -1;
    tk->im_end = -1;
    tk->hdr_start = -1;
    tk->hdr_end = -1;
    c->head_dim = 0;

    *out_merges = NULL;
    *out_n_merges = 0;

    uint32_t alignment = 32;

    for (uint64_t i = 0; i < n_kv; i++) {
        char *key = rstr(&r, NULL);
        uint32_t vt = ru32(&r);

        if      (!strcmp(key, "general.alignment"))                            { alignment    = (uint32_t)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.embedding_length"))                     { c->dim        = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.feed_forward_length"))                  { c->hidden_dim = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.block_count"))                          { c->n_layers   = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.attention.head_count"))                 { c->n_heads    = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.attention.head_count_kv"))              { c->n_kv_heads = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.attention.key_length"))                 { c->head_dim   = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.attention.layer_norm_rms_epsilon"))     { c->norm_eps   = read_float_val(&r, vt); }
        else if (!strcmp(key, "qwen3vl.rope.freq_base"))                       { c->rope_theta = read_float_val(&r, vt); }
        else if (!strcmp(key, "tokenizer.ggml.bos_token_id"))                  { tk->bos       = (int)read_int_val(&r, vt); }
        else if (!strcmp(key, "tokenizer.ggml.eos_token_id"))                  { tk->eos       = (int)read_int_val(&r, vt); tk->eot = tk->eos; }
        else if (!strcmp(key, "tokenizer.ggml.tokens")) {
            if (vt != GV_ARR) { skip(&r, vt); free(key); continue; }
            ru32(&r);
            uint64_t n = ru64(&r);
            tk->size = (int)n;
            c->vocab_size = (int)n;
            tk->vocab = (char **)calloc(n, sizeof(char *));
            tk->vlen  = (int *)calloc(n, sizeof(int));
            for (uint64_t j = 0; j < n; j++)
                tk->vocab[j] = rstr(&r, &tk->vlen[j]);
        }
        else if (!strcmp(key, "tokenizer.ggml.scores")) {
            if (vt != GV_ARR) { skip(&r, vt); free(key); continue; }
            ru32(&r);
            uint64_t n = ru64(&r);
            tk->scores = (float *)malloc(n * sizeof(float));
            for (uint64_t j = 0; j < n; j++) tk->scores[j] = rf32(&r);
        }
        else if (!strcmp(key, "tokenizer.ggml.merges")) {
            if (vt != GV_ARR) { skip(&r, vt); free(key); continue; }
            ru32(&r);
            uint64_t n = ru64(&r);
            *out_n_merges = (int)n;
            *out_merges = (char **)malloc(n * sizeof(char *));
            for (uint64_t j = 0; j < n; j++)
                (*out_merges)[j] = rstr(&r, NULL);
        }
        else { skip(&r, vt); }

        free(key);
    }

    if (c->head_dim == 0)
        c->head_dim = c->dim / c->n_heads;
    c->kv_dim = c->n_kv_heads * c->head_dim;
    c->kv_mul = c->n_heads / c->n_kv_heads;

    m->nti = (int)n_tensors;
    m->ti  = (TensorInfo *)calloc(n_tensors, sizeof(TensorInfo));
    for (uint64_t i = 0; i < n_tensors; i++) {
        m->ti[i].name   = rstr(&r, NULL);
        m->ti[i].n_dims = (int)ru32(&r);
        for (int d = 0; d < m->ti[i].n_dims; d++)
            m->ti[i].ne[d] = ru64(&r);
        m->ti[i].type   = (int)ru32(&r);
        m->ti[i].offset = ru64(&r);
    }

    m->doff = (r.p + (alignment - 1)) & ~(uint64_t)(alignment - 1);
}

/* ================================================================
 * Tokenizer Initialization (rank-based scoring from BPE merges)
 * ================================================================ */

static void init_tokenizer(Tok *tk, char **merges, int n_merges) {
    if (!tk->scores && tk->size > 0)
        tk->scores = (float *)calloc(tk->size, sizeof(float));
    tok_build_hash(tk);
    build_byte_tokens(tk);

    if (n_merges > 0 && merges) {
        for (int i = 0; i < n_merges; i++) {
            char *sp = strchr(merges[i], ' ');
            if (!sp) { free(merges[i]); continue; }
            int la = (int)(sp - merges[i]);
            int lb = (int)strlen(sp + 1);
            char *buf = (char *)malloc(la + lb + 1);
            memcpy(buf, merges[i], la);
            memcpy(buf + la, sp + 1, lb);
            buf[la + lb] = 0;
            int id = tok_lookup(tk, buf, la + lb);
            if (id >= 0)
                tk->scores[id] = (float)(n_merges - i);
            free(buf);
            free(merges[i]);
        }
        free(merges);
    }

    tk->im_start = tok_find_special(tk, "<|im_start|>");
    tk->im_end   = tok_find_special(tk, "<|im_end|>");
    tk->hdr_start = tk->im_start;
    tk->hdr_end   = tk->im_end;
    if (tk->eot < 0 || tk->eot >= tk->size)
        tk->eot = (tk->im_end >= 0) ? tk->im_end : tk->eos;

    printf("Tokenizer: %d tokens | BOS=%d EOS=%d EOT=%d im_start=%d im_end=%d\n",
           tk->size, tk->bos, tk->eos, tk->eot, tk->im_start, tk->im_end);
}

/* ================================================================
 * BPE Encoder (rank-based score merging)
 * ================================================================ */

static int *bpe_encode(Tok *tk, const char *text, int text_len, int *out_n) {
    int *tokens = (int *)malloc((text_len + 1) * sizeof(int));
    int n = 0;

    for (int i = 0; i < text_len; i++) {
        int id = tk->byte_tok[(unsigned char)text[i]];
        if (id >= 0) {
            tokens[n++] = id;
        } else {
            fprintf(stderr, "Warning: byte 0x%02x not in vocabulary\n",
                    (unsigned char)text[i]);
        }
    }

    char buf[MAX_PROMPT_TOKS];
    while (n > 1) {
        float best_score = -1e20f;
        int best_id = -1, best_idx = -1;
        for (int i = 0; i < n - 1; i++) {
            int total = tk->vlen[tokens[i]] + tk->vlen[tokens[i + 1]];
            if (total >= (int)sizeof(buf)) continue;
            memcpy(buf, tk->vocab[tokens[i]], tk->vlen[tokens[i]]);
            memcpy(buf + tk->vlen[tokens[i]], tk->vocab[tokens[i + 1]], tk->vlen[tokens[i + 1]]);
            int id = tok_lookup(tk, buf, total);
            if (id >= 0 && tk->scores[id] > best_score) {
                best_score = tk->scores[id];
                best_id = id;
                best_idx = i;
            }
        }
        if (best_idx < 0) break;
        tokens[best_idx] = best_id;
        for (int i = best_idx + 1; i < n - 1; i++) tokens[i] = tokens[i + 1];
        n--;
    }

    *out_n = n;
    return tokens;
}

/* ================================================================
 * ChatML Encoder (Qwen3-VL text-only)
 *   <|im_start|>system\nYou are a helpful assistant.<|im_end|>\n
 *   <|im_start|>user\n{prompt}<|im_end|>\n
 *   <|im_start|>assistant\n
 * ================================================================ */

static void append_bpe(Tok *tk, int *out, int *n, const char *text) {
    int nt;
    int *t = bpe_encode(tk, text, (int)strlen(text), &nt);
    for (int i = 0; i < nt; i++) out[(*n)++] = t[i];
    free(t);
}

static int *chat_encode(Tok *tk, const char *prompt, int *out_n) {
    int *toks = (int *)malloc(MAX_PROMPT_TOKS * sizeof(int));
    int n = 0;

    /* System message */
    if (tk->im_start >= 0) toks[n++] = tk->im_start;
    append_bpe(tk, toks, &n, "system\nYou are a helpful assistant.");
    if (tk->im_end >= 0) toks[n++] = tk->im_end;
    append_bpe(tk, toks, &n, "\n");

    /* User message */
    if (tk->im_start >= 0) toks[n++] = tk->im_start;
    append_bpe(tk, toks, &n, "user\n");
    append_bpe(tk, toks, &n, prompt);
    if (tk->im_end >= 0) toks[n++] = tk->im_end;
    append_bpe(tk, toks, &n, "\n");

    /* Assistant header */
    if (tk->im_start >= 0) toks[n++] = tk->im_start;
    append_bpe(tk, toks, &n, "assistant\n");

    *out_n = n;
    return toks;
}

/* ================================================================
 * Weight Loading — mmap quantized tensors (main-omp.c parity) plus one
 * reusable BF16 SHMEM scratch sized to the largest *text-stack* GEMV tensor.
 * ================================================================ */

static void npu_create_bo(XdnaDev *dev, XdnaBo *bo, uint32_t type, size_t size);
static void npu_destroy_bo(XdnaDev *dev, XdnaBo *bo);

static void *find_tensor_mmap(Model *m, const char *name, int *out_type) {
    TensorInfo *ti = ti_find(m, name);
    if (!ti) return NULL;
    if (out_type) *out_type = ti->type;
    return (void *)(m->fdata + m->doff + ti->offset);
}

static void bump_gemv_max(Model *m, const char *name) {
    TensorInfo *ti = ti_find(m, name);
    if (!ti) return;
    size_t n = ti_nelements(ti);
    if (n > m->gemv_max_elems) m->gemv_max_elems = n;
}

static size_t row_bytes_quant(int type, int n_in) {
    int nb = n_in / QK_K;
    switch (type) {
    case DT_Q4_K:  return (size_t)nb * sizeof(BlockQ4_K);
    case DT_Q5_K:  return (size_t)nb * sizeof(BlockQ5_K);
    case DT_IQ2_S: return (size_t)nb * sizeof(BlockIQ2_S);
    case DT_IQ3_S: return (size_t)nb * sizeof(BlockIQ3_S);
    default: return 0;
    }
}

static size_t block_size_quant(int type) {
    switch (type) {
    case DT_Q4_K:  return sizeof(BlockQ4_K);
    case DT_Q5_K:  return sizeof(BlockQ5_K);
    case DT_IQ2_S: return sizeof(BlockIQ2_S);
    case DT_IQ3_S: return sizeof(BlockIQ3_S);
    default: return 0;
    }
}

static void dequant_one_block_to_xdna(const void *blk, int type, float *dst) {
    switch (type) {
    case DT_Q4_K:  dequant_q4_k ((const BlockQ4_K *)blk,  dst, 1); break;
    case DT_Q5_K:  dequant_q5_k ((const BlockQ5_K *)blk,  dst, 1); break;
    case DT_IQ2_S: dequant_iq2_s((const BlockIQ2_S *)blk, dst, 1); break;
    case DT_IQ3_S: dequant_iq3_s((const BlockIQ3_S *)blk, dst, 1); break;
    default:
        XDNA_DIE("dequant_one_block_to: bad type %d", type);
    }
}

static void emb_lookup_xdna(float *o, const void *w, int type, int id, int dim) {
    if (dim % QK_K && (type == DT_Q4_K || type == DT_Q5_K || type == DT_IQ2_S || type == DT_IQ3_S))
        XDNA_DIE("emb_lookup: dim %% QK_K != 0 for quant");
    switch (type) {
    case DT_F32:
        memcpy(o, (const float *)w + (size_t)id * dim, (size_t)dim * sizeof(float));
        break;
    case DT_F16: {
        const uint16_t *row = (const uint16_t *)w + (size_t)id * dim;
#ifdef _OPENMP
        #pragma omp parallel for schedule(static)
#endif
        for (int i = 0; i < dim; i++) o[i] = host_f16f32(row[i]);
        break;
    }
    case DT_Q4_K: case DT_Q5_K: case DT_IQ2_S: case DT_IQ3_S: {
        int nb = dim / QK_K;
        size_t row_sz = row_bytes_quant(type, dim);
        size_t bs = block_size_quant(type);
        const uint8_t *row = (const uint8_t *)w + (size_t)id * row_sz;
#ifdef _OPENMP
        #pragma omp parallel for schedule(static)
#endif
        for (int b = 0; b < nb; b++) {
            float blk[QK_K];
            dequant_one_block_to_xdna(row + (size_t)b * bs, type, blk);
            memcpy(o + (size_t)b * QK_K, blk, QK_K * sizeof(float));
        }
        break;
    }
    default:
        XDNA_DIE("Unsupported emb type %d", type);
    }
}

static void load_weights_xdna(Model *m) {
    Config *c = &m->cfg;
    WeightsDev *wd = &m->wd;
    int L = c->n_layers;

    memset(wd, 0, sizeof(*wd));
    m->gemv_max_elems = 0;

    bump_gemv_max(m, "token_embd.weight");
    bump_gemv_max(m, "output.weight");

    wd->embd = find_tensor_mmap(m, "token_embd.weight", &wd->embd_t);
    if (!wd->embd) XDNA_DIE("missing token_embd.weight");

    wd->norm_att = (float **)calloc((size_t)L, sizeof(float *));
    wd->q_norm   = (float **)calloc((size_t)L, sizeof(float *));
    wd->k_norm   = (float **)calloc((size_t)L, sizeof(float *));
    wd->wq       = (void **)calloc((size_t)L, sizeof(void *));
    wd->wq_t     = (int *)calloc((size_t)L, sizeof(int));
    wd->wk       = (void **)calloc((size_t)L, sizeof(void *));
    wd->wk_t     = (int *)calloc((size_t)L, sizeof(int));
    wd->wv       = (void **)calloc((size_t)L, sizeof(void *));
    wd->wv_t     = (int *)calloc((size_t)L, sizeof(int));
    wd->wo       = (void **)calloc((size_t)L, sizeof(void *));
    wd->wo_t     = (int *)calloc((size_t)L, sizeof(int));
    wd->norm_ffn = (float **)calloc((size_t)L, sizeof(float *));
    wd->gate     = (void **)calloc((size_t)L, sizeof(void *));
    wd->gate_t   = (int *)calloc((size_t)L, sizeof(int));
    wd->up       = (void **)calloc((size_t)L, sizeof(void *));
    wd->up_t     = (int *)calloc((size_t)L, sizeof(int));
    wd->down     = (void **)calloc((size_t)L, sizeof(void *));
    wd->down_t   = (int *)calloc((size_t)L, sizeof(int));

    char name[128];
    for (int l = 0; l < L; l++) {
        sprintf(name, "blk.%d.attn_norm.weight", l);
        wd->norm_att[l] = (float *)find_tensor_mmap(m, name, NULL);
        sprintf(name, "blk.%d.attn_q.weight", l);
        bump_gemv_max(m, name);
        wd->wq[l] = find_tensor_mmap(m, name, &wd->wq_t[l]);
        sprintf(name, "blk.%d.attn_k.weight", l);
        bump_gemv_max(m, name);
        wd->wk[l] = find_tensor_mmap(m, name, &wd->wk_t[l]);
        sprintf(name, "blk.%d.attn_v.weight", l);
        bump_gemv_max(m, name);
        wd->wv[l] = find_tensor_mmap(m, name, &wd->wv_t[l]);
        sprintf(name, "blk.%d.attn_output.weight", l);
        bump_gemv_max(m, name);
        wd->wo[l] = find_tensor_mmap(m, name, &wd->wo_t[l]);
        sprintf(name, "blk.%d.attn_q_norm.weight", l);
        wd->q_norm[l] = (float *)find_tensor_mmap(m, name, NULL);
        sprintf(name, "blk.%d.attn_k_norm.weight", l);
        wd->k_norm[l] = (float *)find_tensor_mmap(m, name, NULL);
        sprintf(name, "blk.%d.ffn_norm.weight", l);
        wd->norm_ffn[l] = (float *)find_tensor_mmap(m, name, NULL);
        sprintf(name, "blk.%d.ffn_gate.weight", l);
        bump_gemv_max(m, name);
        wd->gate[l] = find_tensor_mmap(m, name, &wd->gate_t[l]);
        sprintf(name, "blk.%d.ffn_up.weight", l);
        bump_gemv_max(m, name);
        wd->up[l] = find_tensor_mmap(m, name, &wd->up_t[l]);
        sprintf(name, "blk.%d.ffn_down.weight", l);
        bump_gemv_max(m, name);
        wd->down[l] = find_tensor_mmap(m, name, &wd->down_t[l]);

        if (!wd->norm_att[l] || !wd->wq[l] || !wd->wk[l] || !wd->wv[l] ||
            !wd->wo[l] || !wd->q_norm[l] || !wd->k_norm[l] || !wd->norm_ffn[l] ||
            !wd->gate[l] || !wd->up[l] || !wd->down[l])
            XDNA_DIE("layer %d missing weight tensor", l);
    }

    wd->norm_out = (float *)find_tensor_mmap(m, "output_norm.weight", NULL);
    wd->out = find_tensor_mmap(m, "output.weight", &wd->out_t);
    if (!wd->out) {
        wd->out = wd->embd;
        wd->out_t = wd->embd_t;
        wd->tied_output = 1;
    }

    if (!wd->norm_out || !wd->out)
        XDNA_DIE("missing output tensors");

    printf("Weights: mmap GGUF tensors (text GEMV max %zu elements → BF16 scratch)\n",
           m->gemv_max_elems);
}

static void alloc_weight_scratch(Model *m) {
    if (m->gemv_max_elems == 0) XDNA_DIE("gemv_max_elems is zero");
    WeightsDev *wd = &m->wd;
    size_t nbytes = m->gemv_max_elems * sizeof(uint16_t);
    printf("Alloc GEMV BF16 scratch: %zu elems (%.1f MiB BO) + FP32 staging (%.1f MiB)\n",
           m->gemv_max_elems,
           nbytes / (1024.0 * 1024.0),
           (double)(m->gemv_max_elems * sizeof(float)) / (1024.0 * 1024.0));

    wd->scratch_f32 = (float *)aligned_alloc(64, ((m->gemv_max_elems * sizeof(float)) + 63) & ~(size_t)63);
    if (!wd->scratch_f32) XDNA_DIE("scratch_f32 alloc failed");

    memset(&wd->w_scratch_bo, 0, sizeof(wd->w_scratch_bo));
    npu_create_bo(&m->npu, &wd->w_scratch_bo, AMDXDNA_BO_SHMEM, nbytes);
    if (!wd->w_scratch_bo.mapped) XDNA_DIE("scratch BO not mapped");
}

