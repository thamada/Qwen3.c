/*
 * Qwen3-VL GGUF Inference Engine — XDNA2 NPU (amdxdna direct ioctl) backend
 * for AMD Ryzen AI processors (Phoenix / Hawk Point / Strix Point).
 *
 * Target model: Qwen_Qwen3-VL-8B-Instruct-IQ2_M.gguf
 *
 *   - architecture key prefix: qwen3vl.*
 *   - GQA: n_heads=32, n_kv_heads=8, head_dim=128, dim=4096, hidden_dim=12288, layers=36
 *   - Q/K RMSNorm per head (Qwen3 specific) applied after wq/wk linear, before RoPE.
 *   - Tied embedding: NO (token_embd and output.weight are distinct tensors).
 *   - Tokenizer: GPT-2 byte-level BPE (Qwen2 pre-tokenizer; vocab 151936).
 *   - Chat template: ChatML  <|im_start|>role\n...<|im_end|>\n  (text-only path).
 *   - Vision deepstack (n_deepstack_layers=3) is IGNORED — pure text inference.
 *
 *
 * ============================================================================
 *  Backend design notes (XDNA2 / amdxdna)
 * ============================================================================
 *
 * AMD Ryzen AI integrates an NPU built around the XDNA2 architecture:
 *
 *   - Spatial 2D dataflow array of AI Engine compute tiles
 *       Phoenix / Hawk Point : 4 rows x 5 columns
 *       Strix Point          : 4 rows x 8 columns   (XDNA2 generation)
 *   - Each compute tile contains a VLIW SIMD vector processor + 64 KB SRAM
 *       and supports up to 256 INT8 MAC/cycle, with INT8 / BF16 / FP16 / FP32
 *       mixed-precision operations.
 *   - Per column: an L2 memory tile and a dedicated DMA engine moving data
 *       between host DDR and the column's memory tile.
 *   - Strix Point has 4096 KB of total L2 (vs. 2560 KB on Phoenix / Hawk Point).
 *
 * The NPU is managed by the in-tree Linux amdxdna driver (drivers/accel/amdxdna)
 * which exposes a DRM accelerator device node /dev/accel/accelN. Userspace
 * drives the NPU through the DRM ioctl interface declared in
 * <drm/amdxdna_accel.h>:
 *
 *   DRM_IOCTL_AMDXDNA_CREATE_HWCTX  -- create a workload hardware context
 *                                       bound to a spatial partition of cols
 *   DRM_IOCTL_AMDXDNA_CONFIG_HWCTX  -- attach CU configuration (overlay)
 *   DRM_IOCTL_AMDXDNA_CREATE_BO    -- allocate BO: SHMEM / DEV / DEV_HEAP / CMD
 *   DRM_IOCTL_AMDXDNA_GET_BO_INFO  -- get BO map_offset, vaddr, xdna_addr
 *   DRM_IOCTL_AMDXDNA_SYNC_BO      -- sync to/from device (cache coherence)
 *   DRM_IOCTL_AMDXDNA_EXEC_CMD     -- submit ERT command buffers
 *   DRM_IOCTL_AMDXDNA_GET_INFO     -- query AIE metadata, telemetry
 *
 * NPU firmware ("MERT") creates a per-context "ERT" instance to service that
 * workload. The ERT executes a ctrlcode binary (a sequence of XAie_TxnOpcodes)
 * out of a 64 MiB per-context Instruction Buffer (DEV_HEAP BO).  When userspace
 * submits an EXEC_CMD with type AMDXDNA_CMD_SUBMIT_EXEC_BUF carrying an
 * ert_packet whose opcode is ERT_START_NPU, the ERT runs the referenced
 * ctrlcode which kicks off DMAs between host DDR and the AIE columns and
 * performs the actual compute on the compute tiles.
 *
 * For this reference inference engine the strategy is:
 *
 *  - Keep quantized / narrow weights in the mmap'd GGUF (same residency model
 *    as main-omp.c — no BF16 duplication of every layer at rest).
 *  - On each GEMV, dequantize the needed weight tensor into a single BF16 SHMEM
 *    scratch BO (sized for the largest *text-stack* GEMV blob) plus a FP32 row
 *    buffer reused for fused dequant staging; synchronize to the device and
 *    stream from that scratch the same way as before.
 *  - GEMV submits still allocate ephemeral SHMEM BOs for the activation row
 *    and FP32 logits as before (small); the weight matrix DMA source is always
 *    the reused scratch mapped as a single AMDXDNA_BO_SHMEM.
 *  - For each linear (GEMV) layer the host fills the input BO, builds an
 *    ERT_START_NPU command pointing at a precompiled BF16 GEMV ctrlcode
 *    living in the per-context Instruction Buffer, submits with EXEC_CMD,
 *    waits via the returned drm_syncobj timeline.
 *  - Everything else (RMSNorm, Qwen3 per-head RMSNorm, RoPE, FlashAttention
 *    style decode, SwiGLU, residuals, softmax, sampling) runs on the host
 *    CPU — these are O(dim) or O(seq*head_dim) and are not bandwidth-bound
 *    in the same way as GEMV.
 *  - The actual NPU compute kernel (BF16xBF16 -> FP32 column-wise GEMV) is
 *    a precompiled overlay+ctrlcode pair generated by the AMD IRON / MLIR-AIE
 *    toolchain. This source file expects to find the binary blobs at runtime
 *    in either:
 *        $XDNA_GEMV_DIR/<dim_in>x<dim_out>.{xclbin,ctrl}
 *    or, if missing, falls back to a portable OpenMP-parallelized BF16 GEMV
 *    that produces bit-identical results. The fallback path is taken either
 *    when /dev/accel/accel0 cannot be opened, when the requested matmul shape
 *    has no precompiled kernel available, or when XDNA_FORCE_CPU is set in
 *    the environment. The wiring of CREATE_HWCTX / CONFIG_HWCTX / CREATE_BO /
 *    EXEC_CMD is exercised end-to-end whenever the device is available, so
 *    the surrounding host runtime is the same in both modes.
 *
 * The on-wire ABI (amdxdna_accel.h, ert_packet, amdxdna_cmd_start_npu,
 * amdxdna_cmd_chain, etc.) is implemented locally so this file builds against
 * a stock toolchain without requiring xrt / xdna-driver userspace packages —
 * only a libdrm-compatible <drm/drm.h> header and a Linux kernel >= 6.10.
 *
 * Build:
 *   make build.xdna2 -> qwen3-xdna2 (本ファイル)
 */

